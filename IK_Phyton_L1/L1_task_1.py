#Выполнить логические побитовые операции «И»,
# «ИЛИ» и др. над числами 5 и 6. Выполнить над
# числом 5 побитовый сдвиг вправо и влево на два знака.
print(5, bin(5))
print(6, bin(6))
print(5&6, bin(5 & 6))
print(5|6, bin(5|6))
print(5^6, bin(5^6))
print(6<<2, bin(6<<2))
print(6>>2, bin(6>>2))
print(5<<2, bin(5<<2))
print(6>>2, bin(5>>2))